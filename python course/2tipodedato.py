# Numeros Entero --> (Integrer)
print(30)
# Numeros Decimales --> (Float)
print(30.5)

# Booleano --> (Verdadero o Falso)
True
False

# Lista --> (List) 
# Separada por coma y definido por la llave [].  
# Se puede colocar cualquier tipo de String.
# Agrupa datos.
# Se puede cambiar el dato cuanto quiera.
# Se puede dejar vacias simplemente con llaves [].
[10, 20, 30, 55]
['Hello', 'Bye', "Adios"]
[10, "Hello", True, 10.5]
[]

# Tuplas --> (Tuples)
# Separada por coma y definido por los parentesis ().
# Se puede dejar vacias simplemente con parentesis ().
# Agrupa datos.
# Similar a una lista en sentido de agrupar los datos.
# Se utiliza en datos que no cambian. Son Estaticos, conocidos como inmutables.
(10, 20, 30, 55)
()

# Diccionarios --> (Dictionary)
# Sirve para agrupar distintos tipos de datos con un nombre clave.
# Se define como "clave":"valor"
{
    "name":"Ryan",
    "lastname":"Ray",
    "nickname":"Fazt"
}

# Dato sin definir (None)
# Escribir string tal cual como pone.
None
